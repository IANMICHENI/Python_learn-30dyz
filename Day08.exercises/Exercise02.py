'''  
Number Comparison

Ask the user for two numbers a and b and print:

"a is greater than b"

"a is smaller than b"

"a is equal to b"
'''
# Day 08 Of Learning Python
a = int(input("Enter First Number: "))
b = int(input("Enter Second Number: "))

if a > b:
    print("a is greater than b")
elif a < b:
    print("a is smaller than b ")
else:print("a is equal to b")
